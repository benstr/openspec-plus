#!/usr/bin/env node
/**
 * gen-command-docs.mjs — regenerate docs/commands.md from each module's module.json
 * plus its workflow bodies (modules/<m>/workflows/<id>.md).
 *
 * Zero dependencies. Node >= 18. Paths resolve relative to this file, so it runs
 * from any cwd:
 *
 *   node scripts/gen-command-docs.mjs        (or: npm run docs)
 *
 * docs/commands.md is fully generated — never hand-edit it.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const MODULES_DIR = path.join(ROOT, 'modules');
const OUT_FILE = path.join(ROOT, 'docs', 'commands.md');

const HEADER_COMMENT = '<!-- GENERATED by scripts/gen-command-docs.mjs — do not edit -->';

/** Escape pipes so cell content survives a markdown table. */
const cell = (s) => String(s ?? '').replace(/\|/g, '\\|');

/**
 * Parse a workflow body file: strip YAML frontmatter, then pull
 * - the `**Input**` line (verbatim, single line), and
 * - every top-level numbered step heading of the form `N. **Title**`
 *   (column 0 only, so nested numbered lists are ignored; fenced code is skipped).
 */
function parseWorkflow(file) {
  const raw = fs.readFileSync(file, 'utf8');

  let body = raw;
  if (raw.startsWith('---')) {
    const close = raw.indexOf('\n---', 3);
    if (close !== -1) {
      const nl = raw.indexOf('\n', close + 1);
      body = nl === -1 ? '' : raw.slice(nl + 1);
    }
  }

  let inputLine = null;
  const steps = [];
  let inFence = false;

  for (const line of body.split('\n')) {
    if (/^\s*```/.test(line)) {
      inFence = !inFence;
      continue;
    }
    if (inFence) continue;

    if (inputLine === null && line.startsWith('**Input**')) {
      inputLine = line.trim();
      // A colon-terminated Input line leads into a bullet list we don't extract —
      // mark the truncation instead of leaving the lead-in dangling.
      if (inputLine.endsWith(':')) inputLine += ' …';
      continue;
    }
    const m = line.match(/^(\d+)\.\s+\*\*(.+?)\*\*/);
    if (m) steps.push({ number: m[1], title: m[2] });
  }

  return { inputLine, steps };
}

function readModules() {
  return fs
    .readdirSync(MODULES_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort()
    .filter((name) => fs.existsSync(path.join(MODULES_DIR, name, 'module.json')))
    .map((name) => ({
      dir: path.join(MODULES_DIR, name),
      ...JSON.parse(fs.readFileSync(path.join(MODULES_DIR, name, 'module.json'), 'utf8')),
    }));
}

function render(modules) {
  const out = [];
  out.push(HEADER_COMMENT, '');
  out.push('# Command reference', '');
  out.push(
    'Generated from each module\'s `module.json` and workflow bodies',
    '(`modules/<module>/workflows/<id>.md`). Regenerate with `node scripts/gen-command-docs.mjs`.',
    'Step headings below index the flows — the workflow bodies themselves are the full,',
    'authoritative instructions.',
    ''
  );

  for (const mod of modules) {
    out.push(`## ${mod.name}`, '');
    out.push(`${mod.description} (v${mod.version})`, '');
    out.push('| Id | Name | Description | Argument hint |');
    out.push('|---|---|---|---|');
    for (const c of mod.commands) {
      out.push(
        `| \`${cell(c.id)}\` | ${cell(c.name)} | ${cell(c.description)} | \`${cell(c.argumentHint)}\` |`
      );
    }
    out.push('');

    for (const c of mod.commands) {
      const workflowFile = path.join(mod.dir, 'workflows', `${c.id}.md`);
      if (!fs.existsSync(workflowFile)) {
        throw new Error(`missing workflow body for ${mod.name}/${c.id}: ${workflowFile}`);
      }
      const { inputLine, steps } = parseWorkflow(workflowFile);

      out.push(`### /opsx:${c.id}`, '');
      if (inputLine) out.push(inputLine, '');
      if (steps.length > 0) {
        out.push('Steps:', '');
        for (const s of steps) out.push(`${s.number}. ${s.title}`);
        out.push('');
      }
    }
  }

  return out.join('\n').replace(/\n{3,}/g, '\n\n').trimEnd() + '\n';
}

const modules = readModules();
if (modules.length === 0) {
  console.error(`gen-command-docs: no modules found under ${MODULES_DIR}`);
  process.exit(1);
}

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, render(modules));

const commandCount = modules.reduce((n, m) => n + m.commands.length, 0);
console.log(
  `gen-command-docs: wrote ${path.relative(ROOT, OUT_FILE)} — ${modules.length} module(s), ${commandCount} command(s)`
);
