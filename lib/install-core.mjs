function contextStartMarker(moduleName) {
  return `>>> openspec-plus:${moduleName} >>>`;
}

function contextEndMarker(moduleName) {
  return `<<< openspec-plus:${moduleName} <<<`;
}

function contextManualSnippet(moduleName, snippetLines) {
  return [
    'context: |',
    `  ${contextStartMarker(moduleName)}`,
    ...snippetLines.map((l) => (l ? `  ${l}` : '')),
    `  ${contextEndMarker(moduleName)}`,
  ].join('\n');
}

/**
 * Appends (or replaces, on re-run) the module's context snippet inside the
 * `context: |` block of openspec/config.yaml, wrapped in the literal
 * `>>> openspec-plus:<module> >>>` / `<<< openspec-plus:<module> <<<` marker
 * lines. Creates the file or the `context:` key when absent. Line-based and
 * conservative: an unrecognized structure produces a manual-paste snippet
 * instead of an edit.
 */
export function upsertConfigContext(writer, configPath, moduleName, snippetText) {
  const snippetLines = snippetText.replace(/\s+$/, '').split('\n');
  const blockLines = [contextStartMarker(moduleName), ...snippetLines, contextEndMarker(moduleName)];

  const bail = (why) => {
    writer.manual(
      `Could not safely edit ${configPath} (${why}). Merge this into openspec/config.yaml by hand:`,
      contextManualSnippet(moduleName, snippetLines)
    );
  };

  if (!fs.existsSync(configPath)) {
    const content = ['context: |', ...blockLines.map((l) => (l ? `  ${l}` : ''))].join('\n') + '\n';
    writer.write(configPath, content);
    return;
  }

  const original = fs.readFileSync(configPath, 'utf8');
  const lines = original.split('\n');

  // Re-run: replace an existing marker block in place.
  const startIdx = lines.findIndex((l) => l.trim() === contextStartMarker(moduleName));
  const endIdx = lines.findIndex((l) => l.trim() === contextEndMarker(moduleName));
  if (startIdx !== -1 || endIdx !== -1) {
    if (startIdx === -1 || endIdx === -1 || endIdx < startIdx) {
      bail('marker lines are unpaired or out of order');
      return;
    }
    const indent = lines[startIdx].match(/^\s*/)[0];
    const replacement = blockLines.map((l) => (l ? indent + l : ''));
    lines.splice(startIdx, endIdx - startIdx + 1, ...replacement);
    const replaced = lines.join('\n');
    writer.write(configPath, replaced.endsWith('\n') ? replaced : replaced + '\n');
    return;
  }

  const ctxIdx = lines.findIndex((l) => /^context:/.test(l));
  if (ctxIdx === -1) {
    // No context key: append a fresh block scalar at the end of the file.
    const out = [...lines];
    while (out.length > 0 && out[out.length - 1].trim() === '') out.pop();
    out.push('context: |', ...blockLines.map((l) => (l ? `  ${l}` : '')));
    writer.write(configPath, out.join('\n') + '\n');
    return;
  }

  // Only a literal block scalar (`context: |`, optionally with chomping /
  // indentation indicators) is recognized; anything else gets the bail-out.
  if (!/^context:\s*\|[0-9+-]*\s*(#.*)?$/.test(lines[ctxIdx])) {
    bail('the context key is not a literal block scalar');
    return;
  }

  // Find the block scalar's extent: subsequent blank or indented lines.
  let blockEnd = ctxIdx + 1;
  let contentIndent = null;
  while (blockEnd < lines.length) {
    const line = lines[blockEnd];
    if (line.trim() === '') {
      blockEnd += 1;
      continue;
    }
    if (/^\s/.test(line)) {
      if (contentIndent === null) contentIndent = line.match(/^\s*/)[0];
      blockEnd += 1;
      continue;
    }
    break;
  }
  const indent = contentIndent ?? '  ';

  // Insert after the last non-blank line of the block.
  let insertAt = blockEnd;
  while (insertAt > ctxIdx + 1 && lines[insertAt - 1].trim() === '') insertAt -= 1;
  lines.splice(insertAt, 0, ...blockLines.map((l) => (l ? indent + l : '')));
  const inserted = lines.join('\n');
  writer.write(configPath, inserted.endsWith('\n') ? inserted : inserted + '\n');
}

function rulesManualSnippet(rules) {
  const out = ['rules:'];
  for (const [artifactId, ruleStrings] of Object.entries(rules)) {
    out.push(`  ${artifactId}:`);
    for (const rule of ruleStrings) out.push(`    - ${yamlQuote(rule)}`);
  }
  return out.join('\n');
}

/**
 * Merges rule strings (from a module's config-snippets/rules.json, shape
 * `{"<artifactId>": ["rule", ...]}`) into the `rules:` mapping of
 * openspec/config.yaml. Every injected rule carries the
 * `[openspec-plus:pillars]` prefix; that prefix is the idempotency check —
 * re-runs replace prefixed items in place. Conservative: unrecognized
 * structure produces a manual-paste snippet.
 */
export function mergeConfigRules(writer, configPath, rules) {
  const bail = (why) => {
    writer.manual(
      `Could not safely edit ${configPath} (${why}). Merge this into openspec/config.yaml by hand:`,
      rulesManualSnippet(rules)
    );
  };

  if (!fs.existsSync(configPath)) {
    writer.write(configPath, rulesManualSnippet(rules) + '\n');
    return;
  }

  const original = fs.readFileSync(configPath, 'utf8');
  const lines = original.split('\n');

  if (lines.some((l) => /^rules:\s*[^#\s]/.test(l))) {
    bail('the rules key has an inline value');
    return;
  }

  let rulesIdx = lines.findIndex((l) => /^rules:\s*(#.*)?$/.test(l));
  if (rulesIdx === -1) {
    while (lines.length > 0 && lines[lines.length - 1].trim() === '') lines.pop();
    lines.push('rules:');
    rulesIdx = lines.length - 1;
  }

  const rulesBlockEnd = () => {
    let end = rulesIdx + 1;
    while (end < lines.length && (lines[end].trim() === '' || /^\s/.test(lines[end]))) end += 1;
    return end;
  };

  for (const [artifactId, ruleStrings] of Object.entries(rules)) {
    const blockEnd = rulesBlockEnd();
    const keyRe = new RegExp(`^(\\s+)${escapeRegExp(artifactId)}:(.*)$`);
    let keyIdx = -1;
    let keyIndent = '';
    for (let i = rulesIdx + 1; i < blockEnd; i++) {
      const match = lines[i].match(keyRe);
      if (match) {
        if (match[2].trim() !== '') {
          bail(`rules.${artifactId} has an inline value`);
          return;
        }
        keyIdx = i;
        keyIndent = match[1];
        break;
      }
    }

    if (keyIdx === -1) {
      // Artifact key absent: append it at the end of the rules block.
      let insertAt = blockEnd;
      while (insertAt > rulesIdx + 1 && lines[insertAt - 1].trim() === '') insertAt -= 1;
      lines.splice(
        insertAt,
        0,
        `  ${artifactId}:`,
        ...ruleStrings.map((rule) => `    - ${yamlQuote(rule)}`)
      );
      continue;
    }

    // Find the artifact's item range (lines indented deeper than the key).
    let itemEnd = keyIdx + 1;
    while (itemEnd < blockEnd) {
      const line = lines[itemEnd];
      if (line.trim() === '') {
        itemEnd += 1;
        continue;
      }
      if (line.match(/^\s*/)[0].length <= keyIndent.length) break;
      itemEnd += 1;
    }

    // Idempotency: remove previously injected (prefixed) single-line items;
    // a prefixed line that is not a simple `- ` item means the user reshaped
    // the entry, so bail rather than mangle it.
    for (let i = itemEnd - 1; i > keyIdx; i--) {
      if (!lines[i].includes(PILLARS_RULE_PREFIX)) continue;
      if (!/^\s*- /.test(lines[i])) {
        bail(`an existing ${PILLARS_RULE_PREFIX} rule under rules.${artifactId} is not a single-line list item`);
        return;
      }
      lines.splice(i, 1);
      itemEnd -= 1;
    }

    let itemIndent = keyIndent + '  ';
    for (let i = keyIdx + 1; i < itemEnd; i++) {
      const match = lines[i].match(/^(\s*)- /);
      if (match) {
        itemIndent = match[1];
        break;
      }
    }

    let insertAt = itemEnd;
    while (insertAt > keyIdx + 1 && lines[insertAt - 1].trim() === '') insertAt -= 1;
    lines.splice(insertAt, 0, ...ruleStrings.map((rule) => `${itemIndent}- ${yamlQuote(rule)}`));
  }

  const text = lines.join('\n');
  writer.write(configPath, text.endsWith('\n') ? text : text + '\n');
}

