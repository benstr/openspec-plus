/**
 * Recursively copies a scaffold tree, never overwriting an existing file.
 * `.gitkeep` files are ordinary files here and are copied too.
 */
export function copyScaffold(writer, srcDir, destDir) {
  if (!fs.existsSync(srcDir)) {
    writer.skip(srcDir, 'scaffold source missing — nothing copied');
    return;
  }
  const entries = fs
    .readdirSync(srcDir, { withFileTypes: true })
    .sort((a, b) => a.name.localeCompare(b.name));
  for (const entry of entries) {
    const src = path.join(srcDir, entry.name);
    const dest = path.join(destDir, entry.name);
    if (entry.isDirectory()) {
      copyScaffold(writer, src, dest);
    } else if (entry.isFile()) {
      writer.copyKeepExisting(src, dest);
    }
  }
}

