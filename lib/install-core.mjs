/** Composes the managed-block content from the detected module set. */
export function buildAgentsBlock(detectedModules) {
  const lines = [AGENTS_START_MARKER, ''];
  lines.push('## openspec-plus');
  lines.push('');
  const moduleNames = detectedModules.map((m) => `\`${m.name}\``).join(', ');
  lines.push(
    `This repo uses openspec-plus modules (${moduleNames}) — installable customizations ` +
      'layered on OpenSpec through its official customization surfaces. The commands below ' +
      'complement the stock `/opsx:*` workflows. This block is regenerated by ' +
      '`install.mjs`; do not edit between the openspec-plus markers.'
  );
  lines.push('');
  lines.push('| Command | Description |');
  lines.push('|---|---|');
  for (const module of detectedModules) {
    for (const command of module.commands) {
      lines.push(`| \`/opsx:${command.id}\` | ${command.description} |`);
    }
  }

  const names = new Set(detectedModules.map((m) => m.name));
  if (names.has('openspec-pillars')) {
    lines.push('');
    lines.push(
      '**Authority model.** Before planning or authoring artifacts, read ' +
        '`openspec/pillars/DEFINITIONS.md` (vocabulary) first, then `openspec/pillars/README.md` ' +
        '(reading order). Pillars ≻ specs: pillars are timeless beliefs, `openspec/specs/` is ' +
        'current truth — when they disagree, surface the conflict instead of papering over it. ' +
        'Pillar meaning changes are loud and owner-gated: mark the old belief superseded with ' +
        'a pointer, never silently rewrite it.'
    );
  }
  if (names.has('openspec-backlog')) {
    lines.push('');
    lines.push(
      '**Order model.** `openspec/backlog/backlog.md` is the ONLY authority for development ' +
        'order, and each row\'s pointer target IS its status. OpenSpec owns status: ' +
        '`openspec list` shows what is in flight, `openspec/changes/archive/` is what is done — ' +
        'never hand-write "done" in the ledger. Read `openspec/backlog/README.md` before ' +
        'touching the ledger.'
    );
  }

  lines.push('');
  lines.push(AGENTS_END_MARKER);
  return lines.join('\n');
}

/**
 * Regenerates the root AGENTS.md managed block between the openspec-plus
 * markers. Creates AGENTS.md when missing; never touches content outside the
 * markers; appends the block when no markers exist yet.
 */
export function regenerateAgentsBlock(writer, targetDir, detectedModules) {
  const agentsPath = path.join(targetDir, 'AGENTS.md');
  if (detectedModules.length === 0) {
    writer.skip(agentsPath, 'no openspec-plus modules detected — managed block not written');
    return;
  }
  const block = buildAgentsBlock(detectedModules);

  if (!fs.existsSync(agentsPath)) {
    writer.write(agentsPath, block + '\n');
    return;
  }

  const original = fs.readFileSync(agentsPath, 'utf8');
  const lines = original.split('\n');
  const startIdx = lines.findIndex((l) => l.trim() === AGENTS_START_MARKER);
  const endIdx = lines.findIndex((l) => l.trim() === AGENTS_END_MARKER);

  if (startIdx !== -1 && endIdx !== -1 && endIdx >= startIdx) {
    lines.splice(startIdx, endIdx - startIdx + 1, ...block.split('\n'));
    writer.write(agentsPath, lines.join('\n'));
    return;
  }
  if (startIdx !== -1 || endIdx !== -1) {
    writer.manual(
      `AGENTS.md has unpaired openspec-plus markers — fix them, then re-run, or paste the block by hand:`,
      block
    );
    return;
  }
  writer.write(agentsPath, original.replace(/\s+$/, '') + '\n\n' + block + '\n');
}

