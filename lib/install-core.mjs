const POINTER_BLOCK = `${POINTER_GUARD}
> Before starting any work, read [AGENTS.md](AGENTS.md) — authority model, planning
> workflow, and repo vocabulary live there.
`;

/**
 * Prepends the AGENTS.md pointer to a root instruction file — only if the
 * file already exists and does not carry the pointer guard yet.
 */
export function prependPointer(writer, filePath) {
  if (!fs.existsSync(filePath)) {
    writer.skip(filePath, 'file does not exist — pointer not added');
    return;
  }
  const original = fs.readFileSync(filePath, 'utf8');
  if (original.includes(POINTER_GUARD)) {
    writer.keep(filePath, 'pointer already present');
    return;
  }
  writer.write(filePath, POINTER_BLOCK + '\n' + original);
}

