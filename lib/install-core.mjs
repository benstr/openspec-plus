/**
 * Creates the writer every file-system mutation flows through. Respects
 * dry-run mode and collects the written / kept / skipped / manual-step
 * summary printed at the end of a run.
 */
export function createWriter({ dryRun = false } = {}) {
  const summary = { written: [], kept: [], skipped: [], manual: [] };

  /**
   * Writes a file (creating parent directories). With `guardGenerated`, an
   * existing file that does not carry the openspec-plus header is left
   * untouched — we only overwrite files we generated ourselves.
   */
  function write(filePath, content, { guardGenerated = false } = {}) {
    if (fs.existsSync(filePath)) {
      const existing = fs.readFileSync(filePath, 'utf8');
      if (existing === content) {
        keep(filePath, 'already up to date');
        return true;
      }
      if (guardGenerated && !existing.includes('generated by openspec-plus')) {
        skip(filePath, 'existing file was not generated by openspec-plus — left untouched');
        return false;
      }
    }
    if (dryRun) {
      // A real run composes sequential surgeries on the same file (e.g. three
      // config.yaml edits) into one write; report each planned file once.
      if (!summary.written.includes(filePath)) {
        console.log(`[dry-run] would write ${filePath}`);
        summary.written.push(filePath);
      }
      return true;
    }
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, content, 'utf8');
    console.log(`  written  ${filePath}`);
    summary.written.push(filePath);
    return true;
  }

  /** Copies a file only if the destination does not exist (scaffold rule). */
  function copyKeepExisting(srcPath, destPath) {
    if (fs.existsSync(destPath)) {
      keep(destPath, 'kept existing');
      return false;
    }
    if (dryRun) {
      console.log(`[dry-run] would write ${destPath}`);
    } else {
      fs.mkdirSync(path.dirname(destPath), { recursive: true });
      fs.copyFileSync(srcPath, destPath);
      console.log(`  written  ${destPath}`);
    }
    summary.written.push(destPath);
    return true;
  }

  function keep(filePath, reason) {
    summary.kept.push({ path: filePath, reason });
    console.log(`  kept     ${filePath} (${reason})`);
  }

  function skip(filePath, reason) {
    summary.skipped.push({ path: filePath, reason });
    console.log(`  skipped  ${filePath} (${reason})`);
  }

  /** Records a step the user must do by hand (conservative bail-outs). */
  function manual(title, snippet = '') {
    summary.manual.push({ title, snippet });
    console.log(`  MANUAL   ${title}`);
  }

  function printSummary() {
    console.log('');
    console.log('Summary');
    console.log(`  written      : ${summary.written.length}${dryRun ? ' (dry-run — nothing touched)' : ''}`);
    console.log(`  kept existing: ${summary.kept.length}`);
    console.log(`  skipped      : ${summary.skipped.length}`);
    console.log(`  manual steps : ${summary.manual.length}`);
    if (summary.manual.length > 0) {
      console.log('');
      console.log('Manual steps required:');
      summary.manual.forEach((step, i) => {
        console.log(`  ${i + 1}. ${step.title}`);
        if (step.snippet) {
          for (const line of step.snippet.split('\n')) {
            console.log(`       ${line}`);
          }
        }
      });
    }
  }

  return { write, copyKeepExisting, keep, skip, manual, printSummary, summary, dryRun };
}

