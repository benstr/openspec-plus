const REQUIRED_MODULE_FIELDS = ['name', 'version', 'description', 'commands', 'scaffold', 'detectFile'];

/**
 * Discovers modules from `<modulesDir>/<module>/module.json`. Returns the
 * parsed specs with a `dir` property pointing at the module folder.
 */
export function loadModules(modulesDir) {
  if (!fs.existsSync(modulesDir)) return [];
  const modules = [];
  const entries = fs
    .readdirSync(modulesDir, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .sort((a, b) => a.name.localeCompare(b.name));
  for (const entry of entries) {
    const jsonPath = path.join(modulesDir, entry.name, 'module.json');
    if (!fs.existsSync(jsonPath)) continue;
    let spec;
    try {
      spec = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));
    } catch (err) {
      throw new Error(`invalid JSON in ${jsonPath}: ${err.message}`);
    }
    for (const field of REQUIRED_MODULE_FIELDS) {
      if (spec[field] === undefined) {
        throw new Error(`${jsonPath}: missing required field "${field}"`);
      }
    }
    modules.push({ ...spec, dir: path.join(modulesDir, entry.name) });
  }
  return modules;
}

/** True when the module's detectFile exists under the target directory. */
export function isModuleDetected(module, targetDir) {
  return fs.existsSync(path.join(targetDir, module.detectFile));
}

