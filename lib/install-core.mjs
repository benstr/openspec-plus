/**
 * Parses a workflow body file: `---` frontmatter with flat `key: value`
 * string pairs, followed by the markdown body. Hand-rolled on purpose — the
 * format is constrained by DESIGN.md, so no YAML library is needed.
 */
export function parseWorkflowFile(filePath) {
  const raw = fs.readFileSync(filePath, 'utf8');
  const lines = raw.split('\n');
  if ((lines[0] ?? '').trim() !== '---') {
    throw new Error(`${filePath}: expected frontmatter opening "---" on line 1`);
  }
  const attrs = {};
  let i = 1;
  for (; i < lines.length; i++) {
    const line = lines[i];
    if (line.trim() === '---') break;
    if (line.trim() === '' || line.trim().startsWith('#')) continue;
    const match = line.match(/^([A-Za-z][\w-]*):\s*(.*)$/);
    if (!match) {
      throw new Error(`${filePath}: cannot parse frontmatter line: "${line}"`);
    }
    let value = match[2].trim();
    if (
      (value.startsWith('"') && value.endsWith('"') && value.length >= 2) ||
      (value.startsWith("'") && value.endsWith("'") && value.length >= 2)
    ) {
      value = value.slice(1, -1);
    }
    attrs[match[1]] = value;
  }
  if (i >= lines.length) {
    throw new Error(`${filePath}: frontmatter never closed with "---"`);
  }
  const body = lines.slice(i + 1).join('\n').replace(/^\n+/, '');
  return { attrs, body };
}

