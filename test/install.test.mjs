    assert.ok(text.includes('id: opsx-backlog'), 'id field');
    assert.ok(text.includes('category: Workflow'), 'category field');
    assert.match(text, /^description: /m);
    assert.ok(text.includes(GENERATED_LINE), 'generated-by line');
