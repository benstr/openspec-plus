/** Per-scenario fixture: an empty project dir plus a dedicated CODEX_HOME. */
function makeFixture() {
  const root = fs.mkdtempSync(path.join(os.tmpdir(), 'osplus-'));
  fixtures.push(root);
  const project = path.join(root, 'project');
  const codexHome = path.join(root, 'codex-home');
  fs.mkdirSync(project, { recursive: true });
  fs.mkdirSync(codexHome, { recursive: true });
  return { root, project, codexHome };
}

/** Spawns install.mjs; works from any cwd; CODEX_HOME always redirected. */
function runInstall(fx, args) {
  return execFileSync(process.execPath, [installPath, ...args, '--target', fx.project], {
    encoding: 'utf8',
    env: { ...process.env, CODEX_HOME: fx.codexHome },
  });
}

function writeConfig(fx, content) {
  fs.mkdirSync(path.join(fx.project, 'openspec'), { recursive: true });
  fs.writeFileSync(path.join(fx.project, 'openspec', 'config.yaml'), content);
}

function writeConcurrencyProfile(fx, value, { raw = false } = {}) {
  const profilePath = path.join(fx.project, BACKLOG_CONCURRENCY_PROFILE_PATH);
  fs.mkdirSync(path.dirname(profilePath), { recursive: true });
  fs.writeFileSync(profilePath, raw ? value : `${JSON.stringify(value, null, 2)}\n`);
}

const read = (...parts) => fs.readFileSync(path.join(...parts), 'utf8');
const exists = (...parts) => fs.existsSync(path.join(...parts));

/** Recursive sorted list of file paths relative to dir (empty dirs omitted). */
function walk(dir, base = dir) {
  if (!fs.existsSync(dir)) return [];
  const out = [];
  const entries = fs
    .readdirSync(dir, { withFileTypes: true })
    .sort((a, b) => a.name.localeCompare(b.name));
  for (const entry of entries) {
    const p = path.join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(p, base));
    else out.push(path.relative(base, p));
  }
  return out;
}

/** Map of relative path -> file content for a whole tree. */
function snapshotTree(dir) {
  const map = {};
  for (const rel of walk(dir)) map[rel] = read(dir, rel);
  return map;
}

function countOccurrences(haystack, needle) {
  let count = 0;
  let idx = 0;
  while ((idx = haystack.indexOf(needle, idx)) !== -1) {
    count += 1;
    idx += needle.length;
  }
  return count;
}

function assertCount(haystack, needle, expected, label) {
  assert.equal(countOccurrences(haystack, needle), expected, `expected ${expected}× ${label ?? needle}`);
}

/** Reads a workflow body (everything after the frontmatter) from the repo. */
function workflowBody(moduleName, id) {
  const raw = read(repoRoot, 'modules', moduleName, 'workflows', `${id}.md`);
  const lines = raw.split('\n');
  assert.equal(lines[0].trim(), '---', `${moduleName}/${id}: frontmatter must open on line 1`);
  const close = lines.findIndex((l, i) => i > 0 && l.trim() === '---');
  assert.notEqual(close, -1, `${moduleName}/${id}: frontmatter never closed`);
  return lines
    .slice(close + 1)
    .join('\n')
    .replace(/^\n+/, '')
    .trimEnd();
}

